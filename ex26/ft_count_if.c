int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	conta;

	i = 0;
	conta = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			conta++;
		i++;
	}
	return (conta);
}

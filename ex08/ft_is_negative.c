#include <unistd.h>

void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char	neg;
	char	pos;

	neg = 'N';
	pos = 'P';
	if (n < 0)
		ft_putchar(neg);
	else
		ft_putchar(pos);
}
